int main(void)
{
	uint16_t SDRAM_In_Buffer[100], SDRAM_Out_Buffer[100]; 
	uint32_t* SDRAM_Address;
	uint8_t	SDRAM_Status, x;

  // Reset of all peripherals, Initializes the Flash interface and the Systick.
  HAL_Init();

  // Configure the system clock
  SystemClock_Config();

  // Initialize all configured peripherals
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_DAC_Init();
  MX_DMA2D_Init();
  MX_FMC_Init();
  MX_I2C1_Init();
  MX_RNG_Init();
  MX_SAI1_Init();
  MX_SPI2_Init();
  MX_SPI5_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_USART2_UART_Init();
  MX_USB_DEVICE_Init();
  MX_USB_HOST_Init();

	// Product Specific Initialisation
	Triggers_Config();
	Encoders_Config();
	CV_IO_Config();
	
	CV_Out_A(&hdac, 2048);
	CV_Out_B(&hdac, 1024);
		
  // Main Loop
  while (1)
  {
		// USB Host Processes
    MX_USB_HOST_Process();

		
		SDRAM_Address = (uint32_t*)1;
		
		for (x=0; x<100; x++)
		{
			SDRAM_In_Buffer[x] = rand();
		}
		
		SDRAM_Status = HAL_SDRAM_Write_16b(&hsdram1, SDRAM_Address, SDRAM_In_Buffer, sizeof SDRAM_In_Buffer);
		SDRAM_Status = HAL_SDRAM_Read_16b(&hsdram1, SDRAM_Address, SDRAM_Out_Buffer, sizeof SDRAM_Out_Buffer);
		
		Trigger_OUT_A(LOW);
	  Trigger_OUT_B(LOW);
  }
}

// _____ Interrupt Service Routines __________________________________________________________________________
// External Input 4
void EXTI4_IRQHandler(void)
{
	// Encoder B Switch
	if (__HAL_GPIO_EXTI_GET_FLAG(GPIO_PIN_4) != RESET) 
	{
		_Encoder_B_Switch();
		__HAL_GPIO_EXTI_CLEAR_IT(GPIO_PIN_4);	// Clear interrupt flag
	}
}

// External Inputs 10-15
void EXTI15_10_IRQHandler(void)
{
	// Trigger in A
	if (__HAL_GPIO_EXTI_GET_FLAG(GPIO_PIN_10) != RESET) 
	{
		_Trigger_IN_A();
		__HAL_GPIO_EXTI_CLEAR_IT(GPIO_PIN_10);	// Clear interrupt flag
	}

	// Trigger in B
	if (__HAL_GPIO_EXTI_GET_FLAG(GPIO_PIN_11) != RESET) 
	{
		_Trigger_IN_B();
		__HAL_GPIO_EXTI_CLEAR_IT(GPIO_PIN_11);	// Clear interrupt flag
	}
	
	// Encoder A Switch
	if (__HAL_GPIO_EXTI_GET_FLAG(GPIO_PIN_14) != RESET) 
	{
		_Encoder_A_Switch();
		__HAL_GPIO_EXTI_CLEAR_IT(GPIO_PIN_14);	// Clear interrupt flag
	}
}

